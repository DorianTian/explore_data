/**
 * Deterministic picker — returns an element from the pool based on
 * an integer index (wraps around via modulo).
 */
export function pick<T>(pool: T[], index: number): T {
  return pool[index % pool.length];
}

/**
 * Generate a realistic Chinese full name from an integer seed.
 * Uses different strides for surname / given-name to reduce repetition.
 */
export function chineseName(index: number): string {
  return pick(SURNAMES, index) + pick(GIVEN_NAMES, index * 7 + 3);
}
